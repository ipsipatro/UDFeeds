import UIKit

extension UIColor {
    
    @nonobjc class var brandBlue: UIColor {
        return UIColor(red: 0.0, green: 74.0 / 255.0, blue: 133.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var primaryBlue: UIColor {
         return UIColor(red: 0.0, green: 112.0 / 255.0, blue: 187.0 / 255.0, alpha: 1.0)
     }
    
    @nonobjc class var medBlue: UIColor {
        return UIColor(red: 0.0, green: 153.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
}
